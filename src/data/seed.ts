/** Build a fresh seeded AppSnapshot (deterministic). */
export function createSeedSnapshot(now: string = SEED_NOW): AppSnapshot {
  const snapshot: AppSnapshot = {
    schemaVersion: CURRENT_SCHEMA_VERSION,
    family,
    meta: {
      createdAt: now,
      lastModified: now,
    },
  }

  assertValidSnapshot(snapshot)
  return snapshot
}

